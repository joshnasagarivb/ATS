// Variables in JavaScript
// Variables are containers for storing data values.
var A=90;
var B=90;
console.log(A);
console.log(B);
console.log(A+B);
var A=100;
var B=200;
console.log(A);
console.log(B);
console.log(A+B);
A=101;
B=201;
console.log(A);
console.log(B);

let C=10;
let D=20;
console.log(C);
console.log(D);
console.log(C+D);
C=30;
console.log(C);
console.log(D);

const E=100;
const F=200;
console.log(E);
console.log(F);
console.log(E+F);






